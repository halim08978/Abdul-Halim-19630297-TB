     private void tampilrental(){
     Object[] baris = {"ID Pelanggan","Nama Pelanggan","Jenis Kelamin","Alamat","Nam Mobil","Jaminan Pelanggan","Nmor HP"};
        
        tabmode = new DefaultTableModel(null,baris);
        tabmode.setRowCount(0);
        java.sql.Connection KoneksiRental = new Koneksi().connect();
         try {
            Statement rental = KoneksiRental.createStatement();
            String query = "SELECT * FROM data_pelanggan";
            ResultSet rs = rental.executeQuery(query);
            
            while (rs.next()){
                String id_pelanggan = rs.getString("id_pelanggan");
                String nama_pelanggan = rs.getString("nama_pelanggan");
                String jenis_kelamin = rs.getString("jenis_kelamin");
                String alamat = rs.getString("alamat");
                String nama_mobil = rs.getString("nama_mobil");
                String jaminan_pelanggan = rs.getString("jaminan_pelanggan");
                String nomor_hp = rs.getString("nomor_hp");
                String[] data = {id_pelanggan,nama_pelanggan,jenis_kelamin,alamat,nama_mobil,jaminan_pelanggan,nomor_hp};
                tabmode.addRow(data);
            }
        } catch (SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Menampilkan Data Gagal", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
        jTable1.setModel(tabmode);
     }


