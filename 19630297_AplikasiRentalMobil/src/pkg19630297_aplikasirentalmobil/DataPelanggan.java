    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String idWhoWantToBeDelete = jTable1.getValueAt(baris, 0).toString();
        java.sql.Connection KoneksiRental = new Koneksi().connect();
        try {
            Statement rental = KoneksiRental.createStatement();
            String query = "DELETE FROM data_pelanggan WHERE id_pelanggan = '"+idWhoWantToBeDelete+"';";
            int berhasil = rental.executeUpdate(query);
            if (berhasil == 1) {
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                tabmode.getDataVector().removeAllElements();
                tampilrental();
                reset();
            }  
            KoneksiRental.close();
        } catch (SQLException ex){
            ex.printStackTrace();
    }//GEN-LAST:event_jButton3ActionPerformed
  }
